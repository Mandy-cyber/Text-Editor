package textEditor.model.enums;

/**
 * Represents a style that an element can adopt
 */
public enum StyleType {
    /**
     * The size of a font
     */
    FONT_SIZE,

    /**
     * The color of a font
     */
    FONT_COLOR,

    /**
     * The font style/family of a font
     */
    FONT_STYLE,

    /**
     * The background color of an element
     */
    BG_COLOR
}
